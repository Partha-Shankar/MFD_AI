@dataclass
class TextRegion:
    """Represents a single detected text region inside a video frame.

    Attributes
    ----------
    frame_id:
        Zero-based index of the frame in which this region was found.
    bbox:
        ``(x, y, w, h)`` bounding box in frame pixel coordinates.
    text:
        Raw OCR text extracted from the region.
    confidence:
        OCR confidence score in [0, 100].
    ai_score:
        Probability that the text was generated by an LLM / AI tool, in [0, 1].
    """

    frame_id: int
    bbox: tuple[int, int, int, int]
    text: str
    confidence: float
    ai_score: float = 0.0


@dataclass
class TextAnalysisResult:
    """Aggregated result of an entire text analysis pass over a video.

    Attributes
    ----------
    regions:
        All detected text regions across all sampled frames.
    mean_ai_score:
        Average ``ai_score`` over all discovered text regions.  0 if no text
        was found.
    font_inconsistency_detected:
        ``True`` when font metrics differ significantly between regions that
        are expected to share a consistent style.
    temporal_flicker_detected:
        ``True`` when on-screen text appears / disappears faster than natural
        human subtitle pacing.
    verdict:
        Brief human-readable summary of the text analysis findings.
    """

    regions: List[TextRegion] = field(default_factory=list)
    mean_ai_score: float = 0.0
    font_inconsistency_detected: bool = False
    temporal_flicker_detected: bool = False
    verdict: str = "NO_TEXT_FOUND"


