def _detect_temporal_flicker(regions: List[TextRegion], fps: float = 25.0) -> bool:
    """Detect text that appears or vanishes faster than natural pacing.

    A subtitle or lower-third that persists for fewer than ~12 video frames
    is considered a flicker event — characteristic of AI frame synthesis.

    Parameters
    ----------
    regions:
        Text regions sorted by ``frame_id``.
    fps:
        Assumed frame rate of the source video.

    Returns
    -------
    bool
        ``True`` if flicker is detected.
    """
    if not regions:
        return False

    frame_ids = sorted({r.frame_id for r in regions})
    min_persistent_frames = max(1, int(fps * 0.48))  # ~12 frames at 25 fps

    # Measure gaps between consecutive frame IDs that contain text
    for i in range(1, len(frame_ids)):
        if frame_ids[i] - frame_ids[i - 1] < min_persistent_frames:
            return True
    return False


