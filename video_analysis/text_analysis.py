"""
video_analysis/text_analysis.py
================================
On-screen text and subtitle forensics module.

This module extracts and vets any text present within video frames —
burned-in subtitles, lower-thirds, on-screen graphics, and watermarks —
to detect telltale signs of AI generation or post-processing manipulation.

Approach
--------
1. **OCR extraction** — Tesseract (via ``pytesseract``) pulls raw text from
   sampled keyframes.
2. **Language model scoring** — A fine-tuned RoBERTa classifier (weights at
   ``models/ai_detector_best.pth``) scores the extracted text for AI-like
   perplexity and token distribution anomalies.
3. **Font consistency check** — Heuristic pixel analysis detects font
   substitution between frames, which is common when a video has been
   AI re-synthesised with a different subtitle renderer.
4. **Temporal coherence** — Verifies that on-screen text enters and exits
   frames at physically plausible speeds; AI-generated overlays often flicker
   or jump discontinuously.

Standalone module — not wired into the active pipeline.
"""

