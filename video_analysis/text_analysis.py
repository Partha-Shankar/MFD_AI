def extract_text_regions(
    video_path: str,
    sample_every_n: int = 16,
) -> List[TextRegion]:
    """Extract text regions from *video_path* by sampling every N-th frame.

    Uses OpenCV to decode frames and pytesseract for OCR.  Only regions that
    pass a minimum area threshold are returned.

    Parameters
    ----------
    video_path:
        Path to the video file.
    sample_every_n:
        Sampling stride; only frames whose index is divisible by this value
        are processed.

    Returns
    -------
    list of TextRegion
        All text regions found across sampled frames.
    """
    logger.info("[text_analysis] Starting text extraction from: %s", video_path)

    regions: List[TextRegion] = []
    cap = cv2.VideoCapture(video_path)
    frame_id = 0

    # Import pytesseract lazily so the module can load even without it installed
    try:
        import pytesseract  # type: ignore
    except ImportError:
        logger.warning("[text_analysis] pytesseract not installed — skipping OCR.")
        cap.release()
        return regions

    while True:
        ret, frame = cap.read()
        if not ret:
            break
        frame_id += 1
        if frame_id % sample_every_n != 0:
            continue

        # Pre-process: grayscale + adaptive threshold to isolate text
        gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
        thresh = cv2.adaptiveThreshold(
            gray, 255, cv2.ADAPTIVE_THRESH_GAUSSIAN_C, cv2.THRESH_BINARY, 11, 2
        )

        # OCR with word-level bounding boxes
        ocr_data = pytesseract.image_to_data(
            thresh, output_type=pytesseract.Output.DICT
        )

        n_boxes = len(ocr_data["text"])
        for i in range(n_boxes):
            conf = float(ocr_data["conf"][i])
            text = str(ocr_data["text"][i]).strip()
            if not text or conf < 30:
                continue
            x, y, w, h = (
                int(ocr_data["left"][i]),
                int(ocr_data["top"][i]),
                int(ocr_data["width"][i]),
                int(ocr_data["height"][i]),
            )
            if w * h < _MIN_TEXT_BOX_AREA:
                continue
            regions.append(
                TextRegion(
                    frame_id=frame_id,
                    bbox=(x, y, w, h),
                    text=text,
                    confidence=conf / 100.0,
                )
            )

    cap.release()
    logger.info("[text_analysis] Extracted %d text regions.", len(regions))
    return regions


def analyze_text(video_path: str) -> TextAnalysisResult:
    """Run a full text forensics pass over *video_path*.

    Steps
    -----
    1. Extract all text regions via OCR.
    2. Score each region's text with the local AI text classifier.
    3. Check font consistency across regions.
    4. Check temporal flicker.
    5. Compute the aggregate verdict.

    Parameters
    ----------
    video_path:
        Path to the video file to analyse.

    Returns
    -------
    TextAnalysisResult
        Fully populated result object.
    """
    regions = extract_text_regions(video_path)

    if not regions:
        logger.info("[text_analysis] No text found in video — returning clean result.")
        return TextAnalysisResult()

    model = _load_text_classifier()

    for region in regions:
        region.ai_score = _score_text_with_model(region.text, model)

    mean_ai = float(np.mean([r.ai_score for r in regions]))
    font_bad = _detect_font_inconsistency(regions)

    # Estimate FPS from OpenCV (avoid re-opening just for this)
    cap = cv2.VideoCapture(video_path)
    fps = cap.get(cv2.CAP_PROP_FPS) or 25.0
    cap.release()

    flicker = _detect_temporal_flicker(regions, fps=fps)

    issues = []
    if mean_ai > 0.6:
        issues.append(f"High AI text probability ({mean_ai:.0%})")
    if font_bad:
        issues.append("Font inconsistency detected across frames")
    if flicker:
        issues.append("Temporal text flicker detected")

    verdict = "; ".join(issues) if issues else "Text appears authentic"

    result = TextAnalysisResult(
        regions=regions,
        mean_ai_score=mean_ai,
        font_inconsistency_detected=font_bad,
        temporal_flicker_detected=flicker,
        verdict=verdict,
    )

    logger.info("[text_analysis] Text analysis complete — verdict: %s", verdict)
    return result
